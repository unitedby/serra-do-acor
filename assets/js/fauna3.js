// popup 1
const menu1 = document.getElementById('menu-inner-fauna1');
const popup1 = document.getElementById('popup-fauna1');
const closeBtn1 = document.getElementById('popup-fauna1-close');

menu1.addEventListener('click', () => {
  popup1.classList.add('active');
});

closeBtn1.addEventListener('click', () => {
  popup1.classList.remove('active');
});

popup1.addEventListener('click', (e) => {
  if (e.target === popup1) {
    popup1.classList.remove('active');
  }
});


